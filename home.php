Welcome to the marsis.planetserver component of PlanetServer/EarthServer.

The MARSIS sounding radar client provides access to MARSIS subsurface data and simulation, through technologies developed within the <a href="http://earthserver.eu">EarthServer</a> project.

Data can be queried with a <a href="LINK-TO-TEXT">text-based form</a> and a <a href="LINK-TO-TEXT">Map-based GUI</a>.

Data are aquired by ESA Mars Express MARSIS. Processing and simulation is done in cooperation with <a href="http://www.inaf.it">INAF</a> and <a href="http://www.jacobs-university.de">Jacobs University Bremen</a> <a href="http://www.clamv.jacobs-university.de">CLAMV</a>.

